public class EquidistantException extends Exception {

    public EquidistantException() {
        super();
    }

    public EquidistantException(String message) {
        super(message);
    }

    public EquidistantException(String message, Throwable cause) {
        super(message, cause);
    }

    public EquidistantException(Throwable cause) {
        super(cause);
    }

}